		  <h1>ICCIE 2015</h1> 
            <p>The 1st International Conference on Computer & Information Engineering (ICCIE 2015) is an attempt to expose the recent advancement and innovation throughout the vast area of computer and information science. It is expected to be an intellectual platform to share ideas and present the latest findings and experiences in the mentioned areas. World&#39s leading creative educators, researchers, consultants, training managers, policy makers, curriculum developers, entrepreneurs, and others in Computer Science Education are likely to participate in the spark discussion.</p>   				  
