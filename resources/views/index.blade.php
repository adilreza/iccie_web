		  <h1>Review Policy</h1> 
            <p>All ICCIE conference submissions will be double blind peer-reviewed by  usually three reviewers and at least two reviewers. 
			All reviewers are experts in the relevant field, specifically matching the conference topics. Papers are subject to a double-blind review.</p> 
            
			<p>For borderline papers based on the review results at least two members of the conference's technical committee will decide 
			whether a paper can be accepted or not, and in which presentation category it will belong to.
            </p>
