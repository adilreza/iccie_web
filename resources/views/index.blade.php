		       <p>The conference will be organized by the department of Computer Science & Engineering of Rajshahi University of Engineering & Technology. Rajshahi University of Engineering & Technology is one of the prestigious Universities in the northern part of Bangladesh offering Engineering and Technological education effectively. </p>
               <p>Prospective authors are invited to submit original technical papers for presentation at the conference and publication in the conference proceedings. All submitted papers will be peer reviewed. The accepted, registered and presented papers will be available in the website and selected papers will be published in reputed journal(s).The official language of the conference is English.</p>   				  
